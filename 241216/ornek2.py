# kullancıdan kısa ve uzun kenarı alınan dikdörtgenin alanı ve çevresi
kisa_kenar = int( input("kisa_kenar'ı giriniz :") )        # kısa kenarı oku
uzun_kenar = int( input("uzun_kenar'ı giriniz :") )        # uzun kenarı oku
alan= kisa_kenar * uzun_kenar                          
cevre= (kisa_kenar + uzun_kenar)*2
print("Alanı : " ,alan,"Çevresi:",cevre)               # Alanı: 200 Çevresi: 60